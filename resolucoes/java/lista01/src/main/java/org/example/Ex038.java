/*Escreva um programa que mostre na tela a seguinte contagem: 
```
6 7 8 9 10 11 Acabou! */



package org.example;


public class Ex038 {
    public static void main(String[] args) {
        Ex038.main();
    }
    public static void main() {
        int i = 6;
        while (i <= 11){
            System.out.println(i);
            i++;
        }
        System.out.println("Acabou!");

    }
}
